let sum = 0;
let number;
do {
    number = Number(prompt("შეიყვანეთ რიცხვი (0-ს შეყვანისას პროგრამა დასრულდება):"));
    sum += number;
} while (number !== 0);
console.log("რიცხვების ჯამი: " + sum);

// 2)

let num = Number(prompt("შეიყვანეთ რიცხვი გამრავლების ტაბულისთვის:"));
console.log(num + "-ის გამრავლების ტაბულა:");
for (let i = 1; i <= 10; i++) {
    console.log(num + " x " + i + " = " + (num * i));
}


// 3)

const secretPassword = "1234";
let userInput;
do {
    userInput = prompt("შეიყვანეთ პაროლი:");
    if (userInput !== secretPassword) {
        console.log("არასწორი პაროლი, სცადეთ ისევ.");
    }
} while (userInput !== secretPassword);
console.log("პაროლი სწორია!");

