const store = {
    apples: 15,
    bananas: 13,
    oranges: 14,
    applyDiscount: function(discountAmount) {
        for (let product in this) {
            if (typeof this[product] === 'number') {
                this[product] -= discountAmount;
            }
        }
    }
};
console.log("საწყისი ფასები:", store);
store.applyDiscount(5);
console.log("ფასდაკლების შემდეგ:", store);
